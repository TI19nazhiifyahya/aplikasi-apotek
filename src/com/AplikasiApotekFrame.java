                        stmDetailTransaksi.clearParameters();
                        stmDetailTransaksi.setString(1, kodeTransaksiTextField.getText());
                        stmDetailTransaksi.setString(2, detailTransaksi.get(i)[1]);
                        stmDetailTransaksi.setInt(3, Integer.parseInt(detailTransaksi.get(i)[3]));
