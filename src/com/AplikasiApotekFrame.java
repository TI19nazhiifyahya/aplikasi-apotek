    private void tampilkanLaporanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tampilkanLaporanButtonActionPerformed
        // TODO add your handling code here:
        if (bulanLaporanComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Silakan pilih bulan!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (tahunLaporanTextField.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Silakan masukkan tahun!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String bulanLaporan = bulanLaporanComboBox.getSelectedItem().toString();
            String tahunLaporan = tahunLaporanTextField.getText();
            
            DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        
            try { 
                PreparedStatement stmLaporan = conn.prepareStatement("SELECT day(tgl_transaksi), SUM((detail_transaksi.jumlah*stok_obat.harga)/1000) AS total\n" +
                        "FROM transaksi\n" +
                        "JOIN detail_transaksi USING(kode_transaksi)\n" +
                        "JOIN stok_obat USING(kode_obat)\n" +
                        "WHERE monthname(transaksi.tgl_transaksi)=? AND year(transaksi.tgl_transaksi)=?\n" +
                        "GROUP BY tgl_transaksi");
                stmLaporan.setString(1, bulanLaporan);
                stmLaporan.setString(2, tahunLaporan);
                rs = stmLaporan.executeQuery();

                if (rs.next()) {
                    dataset.setValue(rs.getFloat(2), "Tanggal", rs.getString(1));
                    while (rs.next()) {
                        dataset.setValue(rs.getFloat(2), "Tanggal", rs.getString(1));
                    }
                    JFreeChart chart = ChartFactory.createBarChart(
                        "Hasil Penjualan Bulanan", 
                        "Tanggal",
                        "Hasil Penjualan (ribuan)", 
                        dataset, 
                        PlotOrientation.VERTICAL,
                        false,
                        true,
                        false);
                    CategoryPlot p = chart.getCategoryPlot();
                    p.setRangeGridlinePaint(Color.black);
                    ChartFrame frame = new ChartFrame("Laporan Penjualan Bulanan", chart);
                    frame.setVisible(true);
                    frame.setSize(1000,600);
                } else {
                    JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                }                                           
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal menampilkan laporan", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_tampilkanLaporanButtonActionPerformed

