    private void submitLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitLoginButtonActionPerformed
        // TODO add your handling code here:
        boolean loginSuccess = false;
        String permissionKaryawan = "";
        
        if (idKaryawanLoginTextField.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Silakan isi ID karyawan!", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (loginPasswordField.getPassword().length == 0) {
            JOptionPane.showMessageDialog(this, "Silakan masukkan password!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try { 
                database = new DbConn();
                conn = database.getMyConnection();
                stm = conn.createStatement();
                rs = stm.executeQuery("SELECT id_karyawan, password, permission FROM karyawan");

                while (rs.next()) {
                    if (idKaryawanLoginTextField.getText().equals(rs.getString(1)) && 
                            String.valueOf(loginPasswordField.getPassword()).equals(rs.getString(2))){
                        loginSuccess = true;
                        id_karyawan = rs.getString(1);
                        permissionKaryawan = rs.getString(3);
                        break;
                    } 
                }

                if (loginSuccess == true) {
                    idKaryawanLoginTextField.setText("");
                    loginPasswordField.setText("");
                    
                    rs = stm.executeQuery("SELECT COUNT(kode_transaksi) FROM transaksi");
                    
                    rs.next();
                    noTransaksi = rs.getInt(1);

                    if (permissionKaryawan.equals("admin")) {
                        registerMenuItem.setEnabled(true);
                        inventoryMenuItem.setEnabled(true);
                        kasirMenuItem.setEnabled(true);
                        laporanMenuItem.setEnabled(true);

                        loginPanel.setVisible(false);
                        loadingPanel.setVisible(true);
                        jalankanProgressBar();
                        inventoryPanel.setVisible(false);
                        kasirPanel.setVisible(false);
                        laporanPanel.setVisible(false);
                    } else {
                        registerMenuItem.setEnabled(false);
                        inventoryMenuItem.setEnabled(false);
                        kasirMenuItem.setEnabled(true);
                        laporanMenuItem.setEnabled(false);

                        loginPanel.setVisible(false);
                        loadingPanel.setVisible(true);
                        jalankanProgressBar();
                        inventoryPanel.setVisible(false);
                        kasirPanel.setVisible(false);
                        laporanPanel.setVisible(false);
                    }
                } else {
                   JOptionPane.showMessageDialog(this, "Username dan/atau password salah!", "Error", JOptionPane.ERROR_MESSAGE); 
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Login gagal", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_submitLoginButtonActionPerformed

